// 获取全量语言包 API
async function fetchLangPack(lang: string) {
  try {
      const data = await apiRequest<Record<string, string>>({
      url: `/system/i18n/pack?lang=${lang}`,
      method: 'get'
    });
    return data;
  } catch (error) {
    console.error('Failed to load i18n pack', error);
    return {};
  }
}

// 初始化 i18n
export async function initI18n() {
  const currentLang = localStorage.getItem('pantheon_lang') || 'zh-CN';
  const remoteResources = await fetchLangPack(currentLang);
  const localResources = fallbackResources[currentLang as keyof typeof fallbackResources]?.translation || {};

  await i18n
    .use(initReactI18next)
    .init({
      resources: {
        ...fallbackResources,
        [currentLang]: {
          translation: {
            ...localResources,
            ...remoteResources,
          },
        }
      },
      lng: currentLang,
      fallbackLng: 'zh-CN',
      interpolation: {
        escapeValue: false,
      },
    });
}

export default i18n;
